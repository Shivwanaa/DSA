class Solution {
public:
vector<vector<int>>dp;
    int check(int i,int j,vector<int>& nums){
        if(i>j){
            return 0;
        }
        if(dp[i][j]!=-1){
            return dp[i][j];
        }
        int ans=-1e9;
        for(int k=i;k<=j;k++){
            int tot=nums[i-1]*nums[j+1]*nums[k]+check(i,k-1,nums)+check(k+1,j,nums);
            ans=max(ans,tot);
        }
        return dp[i][j]=ans;
    }
    int maxCoins(vector<int>& nums) {
        dp=vector<vector<int>>(nums.size()+1,vector<int>(nums.size()+1,-1));
        nums.insert(nums.begin(),1);
        nums.push_back(1);
        return check(1,nums.size()-2,nums);
    }
};

























































































































































































































































































































































































































































































































